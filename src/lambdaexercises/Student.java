package streams.lambdaexercises;

public class Student {
    String name;
    String email;
    int yearStarted;
    boolean isActive;
    public Student(String name, String email, int yearStarted, boolean isActive) {
        this.name = name;
        this.email = email;
        this.yearStarted = yearStarted;
        this.isActive = isActive;
    }
}
